def _infer_product(query: str) -> str | None:
    """Определить ключ продукта по тексту запроса (или None)."""
    lowered = query.lower()
    for needle, product in _PRODUCT_KEYWORDS:
        if needle in lowered:
            return product
    return None


def _infer_technology(query: str) -> str | None:
    """Определить технологию по тексту запроса (или None)."""
    lowered = query.lower()
    for needle, technology in _TECHNOLOGY_KEYWORDS:
        if needle in lowered:
            return technology
    return None


def _infer_intent(query: str) -> str:
    """Определить интент запроса (по умолчанию commercial)."""
    lowered = query.lower()
    for needle, intent in _INTENT_KEYWORDS:
        if needle in lowered:
            return intent
    return "commercial"


def _priority_from_frequency(frequency: int) -> int:
    """Производный приоритет 0..100 из частотности (long-tail тоже ненулевой)."""
    return min(100, 50 + max(frequency, 0) * 5)


def build_teeon_seo_queries() -> tuple[SeoQuery, ...]:
    """Собрать seed-ядро SEO-запросов TEEON в виде объектов :class:`SeoQuery`."""
    queries: list[SeoQuery] = []
    for cluster, items in _SEED_QUERIES.items():
        for text, frequency in items:
            queries.append(
                SeoQuery(
                    query=text,
                    frequency=frequency,
                    cluster=cluster,
                    product=_infer_product(text),
                    technology=_infer_technology(text),
                    intent=_infer_intent(text),
                    priority=_priority_from_frequency(frequency),
                )
            )
    return tuple(queries)


def group_by_cluster(queries: tuple[SeoQuery, ...]) -> dict[str, list[SeoQuery]]:
    """Сгруппировать запросы по кластеру (порядок кластеров — как в seed).

    Внутри кластера запросы отсортированы по убыванию частотности.
    """
    grouped: dict[str, list[SeoQuery]] = {}
    for query in queries:
        grouped.setdefault(query.cluster, []).append(query)
    for items in grouped.values():
        items.sort(key=lambda q: q.frequency, reverse=True)
    return grouped


def get_seed_clusters() -> tuple[str, ...]:
    """Кластеры, реально представленные в seed-ядре (в порядке объявления)."""
    return tuple(_SEED_QUERIES)
